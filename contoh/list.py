# contoh list di Python
daftar = [7, 1, 5, 3, 2, 4, 6, 8, 3, 5]
print(daftar)
print("list memiliki", len(daftar), "elemen")

# akses elemen yang pertama (dengan indeks 0)
print("daftar[0] =", daftar[0])

# menambahkan data ke list
daftar = daftar + [7, 9]
print(daftar)

# mengurutkan
urut = sorted(daftar)
print("terurut:", urut)
